export type { MessageTree };
export type EnMessages = typeof en;

type BrowserHomeLocale = keyof typeof browserHomeI18n;

function withBrowserHome(locale: BrowserHomeLocale, tree: MessageTree): MessageTree {
  return deepMergeMessages(tree, { browser: browserHomeI18n[locale] });
}

export const MESSAGES: Record<Locale, MessageTree> = {
  en,
  'zh-CN': zhCN,
  'zh-TW': withBrowserHome('zh-TW', zhTW),
  ja: withBrowserHome('ja', ja),
  ko: withBrowserHome('ko', ko),
  es: withBrowserHome('es', es),
  fr: withBrowserHome('fr', fr),
};

function resolveMessages(locale: Locale): MessageTree {
  if (locale === 'en') return MESSAGES.en;
  return deepMergeMessages(MESSAGES.en, MESSAGES[locale]);
}

export function t(locale: Locale, key: string): string {
  const effective = getEffectiveLocale(locale);
  const parts = key.split('.');
  let node: unknown = resolveMessages(effective);
  for (const part of parts) {
    if (node && typeof node === 'object' && part in node) {
      node = (node as Record<string, unknown>)[part];
    } else {
      return key;
    }
  }
  return typeof node === 'string' ? node : key;
}

export function defaultText(key: string): string {
  return t('en', key);
}
